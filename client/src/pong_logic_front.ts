
export interface PongGameData {
	leftPaddleY: number;   // vertical position (top) of left paddle
	rightPaddleY: number;  // vertical position (top) of right paddle
	paddleHeight: number;  // Height of both paddles
	paddleWidth: number;   // Width of both paddles
	ballX: number;         // x pos of ball center
	ballY: number;         // y pos of ball center
	ballRadius: number;    // ball radius
	ballSpeed: number;     
}

